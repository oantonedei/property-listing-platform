export class HeaderComponent {
  router = inject(Router);
  userService = inject(UserService);
  userState!: IUserState;
  constructor() {
    this.userService.getUserStates$.subscribe((response) => {
      this.userState = response;
    });
  }
  signUpOnClick() {
    this.router.navigate(['', 'signup']);
  }
  loginOnClick() {
    this.router.navigate(['', 'login']);
  }
  logout() {
    this.userService.setUserState({
      jwt: '',
      _id: '',
      user_email: '',
      user_name: '',
      user_role: '',
    });
    localStorage.removeItem('USER_STATE');
    this.router.navigate(['']);
  }

  //methods for admin header links
  addProperty() {
    this.router.navigate(['properties', 'add']);
  }
  viewRequests() {
    this.router.navigate(['properties','viewrequests']);
  }
  viewProperties() {
    this.router.navigate(['properties']);
  }
  viewNotifications() {
    this.router.navigate(['properties','viewnotifications']);
  }
}
